package com.company;

public class Producto {
    private String nombre;
    private float precio;




    String getNombre() {
        return nombre;
    }

    public void SetNombre(String nuevoNombre) {
        nombre = nuevoNombre;
    }

    public float getPrecio() {
        return precio;
    }

    public void SetPrecio(float nuevoPrecio) {
        precio = nuevoPrecio;

    }
}



